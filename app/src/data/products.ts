export const normalizeCategory = (value: string): string => {
  return value
    .toLowerCase()
    .trim()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
};

const getCategoryProductCount = (categoryId: string): number => {
  return products.filter(
    (product) => normalizeCategory(product.category) === normalizeCategory(categoryId)
  ).length;
};

